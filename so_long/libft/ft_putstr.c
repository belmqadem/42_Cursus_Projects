#include "libft.h"

int	ft_putstr(char *s)
{
	int	count;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	count = 0;
	while (*s)
	{
		write (1, s, 1);
		s++;
		count++;
	}
	return (count);
}
